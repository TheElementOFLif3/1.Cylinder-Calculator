public class Main {
    public static void main(String[] args) {

        // r – poluprečnik osnove valjka
        // H – visina
        // Pi – konstanta Pi, 3.14
        // V = r2 * Pi * H

        int r = 10;
        int H = 50;
        double Pi = 3.14;
        double Volume = r * r * Pi * H;
        System.out.printf("Zapremina valjka rezultat="+Volume);

    }
}